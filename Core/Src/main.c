// Detect encoder button presses
// Had to use a timer for this because this button was really noisy
void HAL_TIM_OC_DelayElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim == &htim3 && htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
	{
		// Menu layer transitions for numeric and color menus
		switch(menu_layer)
		{
			case NUMERIC_PARAMETER_ROOT:
				menu_layer = NUMERIC_PARAMETER_VALUE_SELECTED;
				break;
			case NUMERIC_PARAMETER_VALUE_SELECTED:
				menu_layer = NUMERIC_PARAMETER_ROOT;
				break;
			case COLOR_PALETTE_ROOT:
				menu_layer = HSV_PICKER_ROOT;
				break;
			case HSV_PICKER_ROOT:
				menu_layer = HSV_PICKER_VALUE_SELECTED;
				break;
			case HSV_PICKER_VALUE_SELECTED:
				menu_layer = HSV_PICKER_ROOT;
				break;
			case MOD_MATRIX_ROOT:
				menu_layer = MOD_MATRIX_DESTINATION_SELECTED;
				break;
			case MOD_MATRIX_DESTINATION_SELECTED:
				menu_layer = MOD_MATRIX_AMOUNT_SELECTED;
				break;
			case MOD_MATRIX_AMOUNT_SELECTED:
				menu_layer = MOD_MATRIX_DESTINATION_SELECTED;
				break;
			default:
				break;
		}

		updateMenuC();
	}
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	switch(GPIO_Pin)
	{
		case FX_CHANGE_BTN_Pin:
			populateMenuItemsC();
			// FX_CHANGE_BTN_Pin case allowed to fall through to BACK_BTN_Pin case due to repeated code
		case BACK_BTN_Pin:
			// Reset cursor position
			menuItemIndex = 0;
			switch(menu_layer)
			{
				case NUMERIC_PARAMETER_ROOT:
					menu_layer = COLOR_PALETTE_ROOT;
					break;
				case NUMERIC_PARAMETER_VALUE_SELECTED:
				case COLOR_PALETTE_ROOT:
					menu_layer = NUMERIC_PARAMETER_ROOT;
					break;
				case HSV_PICKER_ROOT:
					menu_layer = COLOR_PALETTE_ROOT;
					break;
				case HSV_PICKER_VALUE_SELECTED:
					menu_layer = HSV_PICKER_ROOT;
					break;
				case MOD_MATRIX_ROOT:
					menu_layer = NUMERIC_PARAMETER_ROOT;
					break;
				case MOD_MATRIX_DESTINATION_SELECTED:
					menu_layer = MOD_MATRIX_ROOT;
					break;
				case MOD_MATRIX_AMOUNT_SELECTED:
					menu_layer = MOD_MATRIX_DESTINATION_SELECTED;
					break;
				default:
					break;
			}
			updateMenuC();
			break;
		case MOD_BTN_Pin:
			if((menu_layer != MOD_MATRIX_ROOT) && (menu_layer != MOD_MATRIX_DESTINATION_SELECTED) && (menu_layer != MOD_MATRIX_AMOUNT_SELECTED))
			{
				menu_layer = MOD_MATRIX_ROOT;
				updateMenuC();
			}
			break;
	}
}

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef * htim)
{
	// Handle encoder movement
	if(htim == &htim1)
	{
		enc_count_prev = enc_count;
		enc_count = htim->Instance->CNT >> 2;

		encoderLastDirectionForward = !(htim->Instance->CR1 & TIM_CR1_DIR);

		// Check if encoder moved
		if(enc_count != enc_count_prev)
		{
			// FX change button held
			if(HAL_GPIO_ReadPin(FX_CHANGE_BTN_GPIO_Port, FX_CHANGE_BTN_Pin) == GPIO_PIN_RESET)
			{
				if(encoderLastDirectionForward)
				{
					effectIndex = (effectIndex < WS2812FX_NUM_EFFECTS - 1) ? effectIndex + 1 : WS2812FX_NUM_EFFECTS - 1;
				}
				else
				{
					effectIndex = (effectIndex > 0) ? effectIndex - 1 : 0;
				}
			}
			else
			{
				// Trigger change based on current menu_layer
				switch(menu_layer)
				{
					case NUMERIC_PARAMETER_ROOT:
					case COLOR_PALETTE_ROOT:
					case HSV_PICKER_ROOT:
					case MOD_MATRIX_ROOT:
					case MOD_MATRIX_DESTINATION_SELECTED:
					case MOD_MATRIX_AMOUNT_SELECTED:
						// Use last encoder movement direction to determine whether to increment or decrement the current value
						if(encoderLastDirectionForward)
						{
							incrementMenuItemIndexC();
						}
						else
						{
							decrementMenuItemIndexC();
						}
						break;
					case NUMERIC_PARAMETER_VALUE_SELECTED:
						if(encoderLastDirectionForward)
						{
							incrementValueC(effectIndex, menuItemIndex, 0);
						}
						else
						{
							decrementValueC(effectIndex, menuItemIndex, 0);
						}

						break;
					case HSV_PICKER_VALUE_SELECTED:
						if(encoderLastDirectionForward)
						{
							incrementValueC(effectIndex, menuItemIndex, HSVPickerIndex);
						}
						else
						{
							decrementValueC(effectIndex, menuItemIndex, HSVPickerIndex);
						}
						break;
				}
			}

			updateMenuC();
		}
	}
}

